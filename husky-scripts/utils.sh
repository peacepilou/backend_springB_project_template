echo_green() {
    echo "\033[0;32m$1\033[0m"
}

echo_yellow() {
    echo "\033[0;33m$1\033[0m"
}

echo_red() {
    echo "\033[0;31m$1\033[0m"
}

echo_separator() {
    echo "\033[0;32m----------------------------------------------\033[0m"
}

echo_separator_general() {
    echo "\033[0;37m----------------------------------------------\033[0m"
}
